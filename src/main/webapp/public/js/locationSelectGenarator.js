    constructor(provinceId, districtId, cityId){
        this.cityId = document.getElementById(cityId);
        this.districtId = document.getElementById(districtId);
        this.provinceId = document.getElementById(provinceId);
        this.init();
    }

    init(){
        this.locationn.map( province => {
            this.provinceId.innerHTML += `<option label="${province.province}" value="${province.province}">`;
            province.districtList.map( dist => {
                this.districtId.innerHTML += `<option label="${dist.district}" value="${dist.district}">`;
                dist.cityList.map( city => {
                    this.cityId.innerHTML += `<option label="${city}" value="${city}">`;
                })
            })
        })
    }
    
    provinceSelect(selectedProvince){
        this.locationn.map( province => {
            if(province.province == selectedProvince){
                province.districtList.map( district => {
                    this.districtId.innerHTML = `<optidistrictId${district.district}" value="${district.district}">`;
                })
            }
        })
    }
